/*Create two integer variables length and breadth and assign values then check if they are square values
or rectangle values. i.e: if both values are equal then it's square otherwise rectangle.*/
void main() {
  //Assigning values to length and breadth
  int length = 9;
  int breadth = 5;

  //Checking the kind of value
  if (length == breadth) {
    print("The given values are Square.");
  } else {
    print("The given values are Rectangle.");
  }
}
