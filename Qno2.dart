/*Take two variables & store age then use if/else condition to determine oldest & youngest among them.*/
void main() {
  int age1 = 15;
  int age2 = 18;

  //determining the oldest and the youngest age
  if (age1 > age2) {
    print("Person 1 is older.");
    print("Person 2 is younger.");
  } else {
    print("Person 1 is younger.");
    print("Person 2 is older.");
  }
}
